package session_4;

public class While_loop {

	public static void main(String[] args) {
		
		// while loop : first check condition and then executes block of code
		
		int i = 1;	// initialization
		
		while(i<=10) {	//condition
			System.out.println("value of i is : " +i );
			i++;	//incre/decre
//			break;
		}
		
		System.out.println("while loop exits when value of is : " +i);

	}

}
